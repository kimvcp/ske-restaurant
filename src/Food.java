public class Food {
	private String nameF;
	private double priceF;

	public Food(String name, double price) {
		this.nameF = name;
		this.priceF = price;
	}

	public String getNameF() {
		return nameF;
	}

	public void setNameF(String nameF) {
		this.nameF = nameF;
	}

	public double getPriceF() {
		return priceF;
	}

	public void setPriceF(double priceF) {
		this.priceF = priceF;
	}
	
}
